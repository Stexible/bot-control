#!/bin/bash
# Цвета
GREEN='\033[0;32m'
RED='\033[0;31m'
NC='\033[0m' # No Color

# Путь к утилите управления ботами.
# Если настроен symlink /usr/local/bin/botctl — можно заменить строку ниже
# на: BOTCTL="botctl"
BOTCTL="$HOME/BOTS/botctl.py"

# 1. Проверка физического монитора (TTY1) и отсутствие графики
if [ -z "$DISPLAY" ] && [ "$XDG_VTNR" = "1" ]; then
    clear
    echo -e "${GREEN}==========================================${NC}"
    echo -e "   СИСТЕМА УПРАВЛЕНИЯ DELL SERVER (TTY1)  "
    echo -e "${GREEN}==========================================${NC}"
    # Статус ботов — список и описания подтягиваются автоматически
    # из botctl, ничего не нужно прописывать руками при добавлении
    # или переименовании бота.
    python3 "$BOTCTL" --report
    echo "------------------------------------------"
    echo "1) Запустить Рабочий стол (GUI)"
    echo "2) Остаться в Консоли (Серверный режим)"
    echo "3) Перезагрузить ноутбук"
    echo "4) Выключить ноутбук"
    echo "------------------------------------------"
    read -p "Выберите вариант (1-4): " choice
    case $choice in
        1)
            echo "Загрузка графической оболочки..."
            sudo systemctl start gdm3
            ;;
        2)
            echo "Вы находитесь в терминале. Чтобы следить за логами ботов:"
            echo "  Список сервисов и их юнитов: python3 $BOTCTL --report"
            echo "  Логи конкретного бота:       journalctl -u <имя-сервиса> -f"
            ;;
        3)
            echo "Перезагрузка..."
            sudo reboot
            ;;
        4)
            echo "Выключение..."
            sudo poweroff
            ;;
        *)
            echo "Выбор не сделан. Работаем в режиме консоли."
            ;;
    esac
# 2. SSH или графика
else
    echo -e "${GREEN}--- DELL SERVER: УДАЛЕННЫЙ ДОСТУП / GUI ---${NC}"
    python3 "$BOTCTL" --report
    echo "------------------------------------------"
fi
